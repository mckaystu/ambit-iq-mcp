/**
 * Reads rules_library rows and prints VIML envelope YAML per rule (stdout).
 * Does not mutate the database unless you run generated SQL yourself.
 *
 * Usage (from ambit-iq-mcp, DATABASE_URL set):
 *   npm run db:export:viml > exports/rules-as-viml.yaml
 *
 * Emit SQL that sets rule_logic.viml_document (review before running):
 *   npm run db:export:viml -- --emit-sql > exports/rules-viml-updates.sql
 *   EMIT_SQL=1 npm run db:export:viml
 */
import { readFileSync, existsSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { stringify } from "yaml";
import { PrismaClient } from "@prisma/client";
import { buildRulesLibraryVimlDocumentUpdateSql } from "../lib/rulesLibraryVimlSql.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.join(__dirname, "..");

function loadEnvFileIfPresent() {
  const envPath = path.join(repoRoot, ".env");
  if (!existsSync(envPath)) return;
  const raw = readFileSync(envPath, "utf8");
  const fromFile = new Map();
  for (const line of raw.split("\n")) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) continue;
    const eq = trimmed.indexOf("=");
    if (eq <= 0) continue;
    const key = trimmed.slice(0, eq).trim();
    let val = trimmed.slice(eq + 1).trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    fromFile.set(key, val);
  }
  for (const [key, val] of fromFile) {
    if (key === "DATABASE_URL") process.env.DATABASE_URL = val;
    else if (process.env[key] === undefined) process.env[key] = val;
  }
}

loadEnvFileIfPresent();

const prisma = new PrismaClient();

function ruleToVimlEnvelope(row) {
  const logic = row.rule_logic && typeof row.rule_logic === "object" ? row.rule_logic : {};
  const ruleId = String(logic.id || row.rule_name || "RULE").trim();
  const pattern = String(logic.pattern || "").trim();
  const domain = String(row.domain_id || "quality");
  const profile =
    domain === "regulatory"
      ? "financial-services.eu"
      : "baseline.global";

  const doc = {
    vibe: {
      intent: String(logic.description || row.rule_name || "Migrated rule from rules_library"),
      priority: String(logic.severity || "MEDIUM"),
      category: domain,
      profile,
      id: ruleId.replace(/[^a-zA-Z0-9_]/g, "_"),
    },
    target: {
      files: ["**/*"],
      tenant_id: row.tenant_id ? String(row.tenant_id) : null,
    },
    enforce: pattern && !["unprotected_network_call", "network_call_without_timeout"].includes(pattern)
      ? [{ id: ruleId, pattern, message: String(logic.action || ""), severity: String(logic.severity || "high") }]
      : [],
    logic: String(row.rego_code || "").trim() || "# No Rego body stored for this row.\ndefault allow := true\n",
    on_failure: String(logic.action || "Policy violation."),
  };

  return { rule_id: row.rule_id, rule_name: row.rule_name, yaml: stringify(doc) };
}

async function main() {
  const emitSql =
    process.argv.includes("--emit-sql") || String(process.env.EMIT_SQL || "").trim() === "1";
  const url = String(process.env.DATABASE_URL || "").trim();
  if (!/^postgres(ql)?:\/\//i.test(url)) {
    console.error("DATABASE_URL must be a postgres connection string.");
    process.exitCode = 1;
    return;
  }
  const rows = await prisma.$queryRaw`
    SELECT rule_id, tenant_id, domain_id, rule_name, rule_logic, rego_code
    FROM rules_library
    WHERE COALESCE(NULLIF(trim(status::text), ''), 'active') IN ('active', 'shadow')
    ORDER BY created_at ASC
  `;
  if (!Array.isArray(rows) || rows.length === 0) {
    console.error("# No rows returned from rules_library (check status / DATABASE_URL).");
    process.exitCode = 1;
    return;
  }
  if (emitSql) {
    console.log("-- Generated by migrate-rules-to-viml-envelope.mjs --emit-sql");
    console.log("-- Merges rule_logic.viml_document (YAML text) per rule_id. Review before executing.\n");
  }
  for (const row of rows) {
    const { rule_id, rule_name, yaml } = ruleToVimlEnvelope(row);
    if (emitSql) {
      console.log(`-- rule_name=${JSON.stringify(rule_name)}`);
      console.log(buildRulesLibraryVimlDocumentUpdateSql(String(rule_id), yaml));
    } else {
      console.log(`# --- rule_id=${rule_id} rule_name=${JSON.stringify(rule_name)} ---`);
      console.log(yaml.trim());
      console.log("");
    }
  }
}

await main()
  .catch((e) => {
    console.error(e);
    process.exitCode = 1;
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
